    //Metodo para calcular nuestro sueldo
    public void calcular(){
        String a,b,contrasena;
        int s,m,r;
        a=txtSueldoBase.getText();
        b=txtMeses.getText();
        s=Integer.parseInt(a);
        m=Integer.parseInt(b);
        //Mensaje de salida que pide la contrase単a
        contrasena=JOptionPane.showInputDialog("Ingrese su contrase単a");
        //Si la constrase単a es igual, se podra hacer el calculo
        if(contrasena.equals("hola")){
        r=(int)((s-(0.1*s))*m);
        etiResultado.setText(""+r);
        }
        //Si la contrase単a es incorrecta no se podra calcular.
        else{
        JOptionPane.showMessageDialog(rootPane, "Contrasena invalida");
        }
}   
    //Metodo para la validacion.
    public void verif(KeyEvent evt){
        char validar = evt.getKeyChar();
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingrese solo numeros");
        }
        }
