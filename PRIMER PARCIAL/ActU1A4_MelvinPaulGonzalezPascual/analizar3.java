    //Metodo para la busqueda
    public void cadenas(){
    //Si esta vacio no se realiza ninguna opcion.    
    if(txtFrase.getText().length()==0){
     JOptionPane.showMessageDialog(rootPane, "Ingrense Datos");
    }else{  
    //Se crean varias cadenas.    
    String cad1=txtFrase.getText();
    String cad2=txtPalabra1.getText();
    String cad3=txtPalabra2.getText();
    String cad4=txtPalabra3.getText();
    String cad5=txtPalabra4.getText();
    //Condicion para ver si es igual
    if(cad1.equals(cad2)){
        etiPalabra1.setText("La palabra es igual a la frase");
        }else{
        etiPalabra1.setText("La palabra no es igual");}
    //Condicion para ver si es comienza
    if(cad1.startsWith(cad3)){
        etiPalabra2.setText("Si comienza");
    }else{
        etiPalabra2.setText("No empieza "); }
    //Condicion para ver si termina
    if(cad1.endsWith(cad4)){
        etiPalabra3.setText("Si termina");
    }else{
        etiPalabra3.setText("No termina ");
    }
    //Condicion para ver si contiene esa palabra
    if( cad1.contains(cad5)){
    etiPalabra4.setText("Encontrado en la posici√≥n: "+cad1.indexOf(cad5));
    }else{etiPalabra4.setText("No se encontro");}
    }
    }
    //Metodo para validar el teclado.
    public void verif(KeyEvent evt){
    char validar = evt.getKeyChar();
        if(Character.isDigit(validar)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Ingrese solo letras");
        }
}
