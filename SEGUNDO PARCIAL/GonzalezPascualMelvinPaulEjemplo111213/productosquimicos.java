    //Metodo para calcular el total de agua.
    public void aguas() {
        double totalagua;
        double litros,calidad,densidad;
        litros=deslizadorLitros.getValue();
        calidad=deslizadorCalidad.getValue();
        densidad=deslizadorDensidad.getValue();
        totalagua=(2*(litros+calidad))/(densidad);///densidad;
        totalagua=Math.round(totalagua*1000d)/1000d;
        etiLitrosAgua.setText(""+totalagua);
        
        
    }
    //Metodo para calcular el total de sales.
    public void sales(){
        double totalsales;
        double litros,calidad,densidad;
        litros=deslizadorLitros.getValue();
        calidad=deslizadorCalidad.getValue();
        densidad=deslizadorDensidad.getValue();
        totalsales=(calidad*litros)/(100*densidad);
        totalsales=Math.round(totalsales*1000d)/1000d;
        etiSales.setText(""+totalsales);
    }

