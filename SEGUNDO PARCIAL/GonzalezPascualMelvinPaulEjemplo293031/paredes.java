    //Se crea el evento donde dependiendo la opcion se le asignaran los valores.
    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        double ancho, alto;
        ancho = Integer.parseInt(txtAncho.getText());
        alto = Integer.parseInt(txtAlto.getText());
        if (rbParedNorte.isSelected()) {
            ParedesNorte.setAltura(alto);
            ParedesNorte.setBase(ancho);
            txtAlto.setText("");
            txtAncho.setText("");
        } else if (rbParedSur.isSelected()) {
            ParedesSur.setAltura(alto);
            ParedesSur.setBase(ancho);
            txtAlto.setText("");
            txtAncho.setText("");
        } else if  (rbParedEste.isSelected()){
            ParedesEste.setAltura(alto);
            ParedesEste.setBase(ancho);
            txtAlto.setText("");
            txtAncho.setText("");
        } else if  (rbParedOeste.isSelected()){
            ParedesOeste.setAltura(alto);
            ParedesOeste.setBase(ancho);
            txtAlto.setText("");
            txtAncho.setText("");
        }
    }//GEN-LAST:event_btnAsignarActionPerformed
        //Metodo para calcular el area de las paredes.
    private void btnAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAreaActionPerformed
        double x = 0;
        if (rbParedNorte.isSelected()) {
            x=ParedesNorte.getArea();
        } else if (rbParedSur.isSelected()) {
            x=ParedesSur.getArea();
        } else if  (rbParedEste.isSelected()){
            x=ParedesEste.getArea();
        } else if  (rbParedOeste.isSelected()){
            x=ParedesOeste.getArea();
        }
        JOptionPane.showMessageDialog(null,"El area de la pared es: "+x);
    }//GEN-LAST:event_btnAreaActionPerformed
