    //Se elimina el renglon seleccionado.
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filasel = tabla.getSelectedRow();
        if (filasel == -1) {
            JOptionPane.showMessageDialog(null, "No hay ninguna fila seleccionada");
        } else {
            m = (DefaultTableModel) tabla.getModel();
            m.removeRow(filasel);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    //Se hace el promedio de la calificacion de la materia del alumno seleccionado.
    private void btnResultadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadosActionPerformed
        int promedio = 0;
        int filasel = tabla.getSelectedRow();
        if (filasel == -1) {
            JOptionPane.showMessageDialog(null, "No hay ninguna fila seleccionada");
        } else {
            int calificacion1 = Integer.parseInt(m.getValueAt(filasel, 1).toString());
            int calificacion2 = Integer.parseInt(m.getValueAt(filasel, 2).toString());
            int calificacion3 = Integer.parseInt(m.getValueAt(filasel, 3).toString());
            promedio = (calificacion1 + calificacion2 + calificacion3) / 3;
            if (promedio > 5) {
                JOptionPane.showMessageDialog(null, "El promedio del curso es: " + promedio + "\nAprobado!");
            } else {
                JOptionPane.showMessageDialog(null, "El promedio del curso es: " + promedio + "\nNo aprobado!");
            }

        }
    }//GEN-LAST:event_btnResultadosActionPerformed
    //Se calcula los aprobado y no aprobados del primer trimestre.
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int filas = m.getRowCount();
        int aprobados = 0;
        int noaprobaods = 0;

        for (int i = 0; i < filas; i++) {
            int dato = Integer.parseInt(m.getValueAt(i, 1).toString());
            if (dato > 5) {
                aprobados++;
            }
            if (dato <= 5) {
                noaprobaods++;
            }
        }
        JOptionPane.showMessageDialog(null, "Numeros del Primer Trimestre" + "\nLa cantidad de " + aprobados + " alumnos han aprobado " + "\nLa cantidad de " + noaprobaods + " alumnos no han aprobado");
    }//GEN-LAST:event_jButton1ActionPerformed

