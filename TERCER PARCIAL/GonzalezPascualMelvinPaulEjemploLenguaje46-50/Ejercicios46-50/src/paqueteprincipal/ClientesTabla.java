    //Boton para hacer los filtros ademas de buscar los datos del cliente.
    private void btnFiltrarAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarAceptarActionPerformed
        String cif,nombre,direccion,telefono1,telefono2;
        String sql;
        int ncond=0;
        try{
        cif=txtFiltrarCIF.getText();
        nombre=txtFiltrarNombre.getText();
        direccion=txtFiltrarDireccion.getText();
        telefono1=txtFiltrarTelefono1.getText();
        telefono2=txtFiltrarTelefono2.getText();
        
        sql="select * from cliente";
        if(!cif.equals("")){
            ncond++;
            sql+=" where dni like '%"+cif+"%' ";
        }
        if(!nombre.equals("")){
            ncond++;
            if(ncond==1){
                sql+=" where nombre like '%"+nombre+"%' ";
            }else{
                sql+=" and nombre like '%"+nombre+"%' ";
            }
        }
        if(!direccion.equals("")){
            ncond++;
            if(ncond==1){
                sql+=" where direccion like '%"+direccion+"%' ";
            }else{
                sql+=" and direccion like '%"+direccion+"%' ";
            }
        }
        if(!telefono1.equals("")){
            ncond++;
            if(ncond==1){
                sql+=" where telefono1 like '%"+telefono1+"%' ";
            }else{
                sql+=" and telefono1 like '%"+telefono1+"%' ";
            }
        }
        if(!telefono2.equals("")){
            ncond++;
            if(ncond==1){
                sql+=" where telefono2 like '%"+telefono2+"%' ";
            }else{
                sql+=" and telefono2 like '%"+telefono2+"%' ";
            }
        }
        //Ordenacion de los resultados o de la tabla.
        String campoorden=(String) cboCamposOrdenacion.getSelectedItem();
        if(!campoorden.equals("(Sin ordenacion)")){
            if(radioASC.isSelected()){
                sql+=" order by "+campoorden+ " asc";
            }else{
                sql+=" order by "+campoorden+ " desc";
            }
        }
        
        ResultSet r=sentencia.executeQuery(sql);
        String titulos[]={"CIF","Nombre","Direccion","Telefono 1","Telefono 2"};
        modelo=new DefaultTableModel(null,titulos);
        String fila[]=new String [5];
        while(r.next()){
        fila[0] = r.getString("cif");
                fila[1] = r.getString("nombre");
                fila[2] = r.getString("direccion");
                fila[3] = r.getString("telefono1");
                fila[4] = r.getString("telefono2");
                modelo.addRow(fila);
        }
        jTabla.setModel(modelo);
        HacerCalculos(r);
        dialogoFiltrar.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al filtrar la tabla clientes");
    }
    }//GEN-LAST:event_btnFiltrarAceptarActionPerformed
