    //Evento para elimanar un cliente.
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filsel;
        String cif;
        int resp;
        try {
            filsel = jTabla.getSelectedRow();
            if (filsel == -1) {
                JOptionPane.showMessageDialog(null, "Debes seleccionar el trabajador a borrar");
            } else {
                resp = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el trabajador seleccionado?", "Eliminar", JOptionPane.YES_NO_OPTION);
                if (resp == JOptionPane.YES_OPTION) {
                    modelo = (DefaultTableModel) jTabla.getModel();
                    cif = (String) modelo.getValueAt(filsel, 0);
                    sentencia.executeUpdate("delete from cliente where cif='" + cif + "'");
                    MostrarClientes();
                }
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar cliente");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
