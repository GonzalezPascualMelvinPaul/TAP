    //Metodo el cual se hace la conexion con nuesta base de datos de Postgresql.
    void PrepararBaseDatos() {
        try {
            String controlador = "org.postgresql.Driver";
            Class.forName(controlador).newInstance();
        } catch (ClassNotFoundException | IllegalAccessException | InstantiationException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar el controlador");
        }
        //se prepara la base de dats
        try {
            //Agregamos la ruta de nuestra base de datos creada.
            String DSN = "jdbc:postgresql://localhost:5432/BDTap?currentSchema=manempsa";
            //jdbc:postgresql://localhost:5432/mydatabase?currentSchema=myschema
            String user = "postgres";
            String password = "melvin";
            conexion = DriverManager.getConnection(DSN, user, password);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al realizar la conexion");
        }
        try {
            sentencia = conexion.createStatement(
                    ResultSet.TYPE_SCROLL_INSENSITIVE,
                    ResultSet.CONCUR_READ_ONLY);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al crear el objeto sentencia");
        }

    }
    //Metodo que nos muestra los datos de la tabla coche.
    void MostrarTodos() {
        String info = "";
        String cadmat, cadmarca, cadmodelo, cadanio, caddni;
        try {
            //Se crea la tabla y se ejecuta la sentencia para poder ejecutar la instruccion.
            ResultSet r = sentencia.executeQuery("select * from coche");
            r.beforeFirst();
            while (r.next()) {
                cadmat = r.getString("matricula");
                cadmarca = r.getString("marca");
                cadmodelo = r.getString("modelo");
                cadanio = r.getString("anio");
                caddni = r.getString("dni");
                info = info + cadmat + " -- " + cadmarca + " -- " + cadmodelo + " -- " + cadanio + " -- " + caddni + "\n";
            }
            panelTexto.setText("");
            panelTexto.setText(info);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al consultar la tabla coche");
        }
    }

