    //Evento del boton para poder elimanar un coche de la tabla de coches.
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String cadmatricula = "";
        String consulta = "";
        int resp;
        try {
            consulta = "select * from coche where matricula=" + "'" + txtMatriculaEliminar.getText() + "'";
            ResultSet r = sentencia.executeQuery(consulta);
            if (!r.first()) {
                JOptionPane.showMessageDialog(null, "No existe ese coche con esa Matricula");
            } else {   
                //Hace la confirmacion para poder eliminar el coche.
                resp = JOptionPane.showConfirmDialog(null, "Â¿Confirma el borrado?", "Borrar", JOptionPane.YES_NO_OPTION);
                if(resp==JOptionPane.YES_OPTION){
                //Se ejecuta la instruccion.
                consulta="delete from coche where matricula='"+txtMatriculaEliminar.getText()+"'";
                sentencia.executeUpdate(consulta);
                MostrarTodos();
                }else{
                JOptionPane.showMessageDialog(null,"Borrado cancelado por el usuario");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Hubo un error al eliminar el coche");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
